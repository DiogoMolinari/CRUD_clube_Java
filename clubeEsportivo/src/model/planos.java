package src.model;

public class planos {
    private int idPlano;
    private String nomePlano, descricaoPlano;

    public int getIdPlano() { return idPlano; }
    public void setIdPlano(int idPlano) { this.idPlano = idPlano; }

    public String getNomePlano() { return nomePlano; }
    public void setNomePlano(String nomePlano) { this.nomePlano = nomePlano; }

    public String getDescricaoPlano() { return descricaoPlano; }
    public void setDescricaoPlano(String descricaoPlano) { this.descricaoPlano = descricaoPlano; }
}
